// Helper: retorna monstros disponíveis para o combat level do jogador
export function getAvailableMonsters(combatLevel, hideSlayer = true) {
  return MONSTERS.filter(m => {
    if (hideSlayer && m.slayerReq) return false
    if (combatLevel < m.minCB) return false
    if (m.maxCB !== undefined && combatLevel > m.maxCB + 15) return false // +15 margem
    return true
  })
}

